// functions example
// https://gobyexample.com/functions

static Integer plus(Integer a, Integer b) {

	return a + b

}

static Integer plusPlus(Integer a, b, c) {
	return a + b + c
}

static void main() {

	Integer res = plus(1, 2)
	println "1+2 = $res"

	Integer res2 = plusPlus(1, 2, 3)
	println "1+2+3 = $res2"

}

//run it
main()

